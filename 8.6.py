def counting_sort(A, k):
    """
    Реалізація сортування підраховуванням.
    A - вхідний масив
    k - максимальне значення в масиві (діапазон [0...k])
    """
    n = len(A)
    # Масив B для збереження відсортованого результату
    B = [0] * n
    # Допоміжний масив C для підрахунку кількості елементів
    # Розмір k+1, щоб включити саме число k
    C = [0] * (k + 1)

    # 1. Підрахунок кількості входжень кожного числа (рядки 3-4 методички)
    for j in range(n):
        C[A[j]] = C[A[j]] + 1

    # 2. Обчислення префіксних сум (позицій) (рядки 6-7 методички)
    # C[i] тепер містить кількість елементів, що не перевищують i
    for i in range(1, k + 1):
        C[i] = C[i] + C[i - 1]

    # 3. Формування вихідного масиву B (рядки 9-11 методички)
    # Йдемо з кінця масиву A для забезпечення СТІЙКОСТІ алгоритму
    for j in range(n - 1, -1, -1):
        B[C[A[j]] - 1] = A[j]  # -1 через індексацію Python з нуля
        C[A[j]] = C[A[j]] - 1

    return B

