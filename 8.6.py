    test_array = [3, 1, 4, 1, 5, 0, 3, 2, 4, 2]
    max_val = max(test_array)
    
    print(f"Вхідний масив: {test_array}")
    sorted_array = counting_sort(test_array, max_val)
    print(f"Відсортований масив: {sorted_array}")